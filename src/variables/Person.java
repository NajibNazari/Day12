package variables;

public class Person {

	
	// static variable is declared inside a class with a STATIC keyword & outside the method body
	// static variable 
	public static double salary;
	public static int id;
	public static String name;
	
	
}


/**
--> we created an OBJECT whenever we wanted to access INSTANCE VARIABLE from another class
--> to Access static variable from another class --> we Do NOT need to create an object
--> for STATIC VARIABLE, it is not required for us to create an object
--> if we are accessing from different class then we can access by 

					ClassName.staticVariableName;

*/